const mongoose = require("mongoose");

//Products Schema
const productSchema = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  pic: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  quantity: {
    type: Number,
    required: true,
  },
  best: {
    type: Boolean,
  },
});

module.exports = mongoose.model("Product", productSchema);
